// No Java, quando está se declarando um classe,
// a primeira letra será maiúscula.
public class ViradoNoJiraya {

	// O Java sempre tem um começo.
	// Ele começará a ser executado a partir daqui.
	// Diferente das classes, os métodos começam com letras minúsculas.
	// Se o método tiver mais de uma palavra, ele seguirá camelCase.
	// Ex.: mainExemploDois
	public static void main(String[] args){
		
		// Esta linha não é um método, é uma linha de execução.
		// Deve-se colocar ";" para sinalizar que o comando terminou.
		System.out.println("Hello, World!");
	}
}

// Sempre salve o arquivo com o nome da classe.
// Neste caso: ViradoNoJiraya.